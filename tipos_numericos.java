package pomarola;

public class tipos_numericos {

	public static void main(String[] args) {
		// tipos primarios/primitivos
		int num1 = 4; // inteiros infinitos9999999999999999999999999999
		short num2 = 4; //inteiros curtos 999999
		
		//tipos não primarios
		Integer num3 = 98;
		short num4 = 7;
		
		System.out.println("int ->num1 =" + num1);
		System.out.println("short ->num2= " + num2);
		System.out.println("integer ->num3= " + num3);
		System.out.println("short -> num4 = " + num4);

	}

}
